public class testFile {
    public int roll;
    public String result;

    public testFile(int roll, String result) {
        this.roll = roll;
        this.result = result;
    }

    public int getRoll() {
        return roll;
    }

    public void setRoll(int roll) {
        this.roll = roll;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }
}
